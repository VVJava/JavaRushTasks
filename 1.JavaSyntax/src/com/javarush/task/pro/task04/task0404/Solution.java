package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        int string=0;
        while (string<5){
            int line = 0;
            while (line<10){
                System.out.print("Q");
                line++;
            }
            System.out.println();
            string++;
        }


    }
}